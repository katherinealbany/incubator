#!/bin/bash -x

###################################################################################################

BASE="$(dirname ${BASH_SOURCE[0]})"

###################################################################################################

${BASE}/uninstall.sh

###################################################################################################

for item in $(kubectl get crd --output=custom-columns=:.metadata.name | grep monitoring.coreos.com); do
  kubectl delete --wait=true crd ${item}
done

###################################################################################################

exit 0

