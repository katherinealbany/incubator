#!/bin/bash -x

###################################################################################################

BASE="$(dirname ${BASH_SOURCE[0]})"

###################################################################################################

${BASE}/uninstall.sh

###################################################################################################

for item in $(kubectl get customresourcedefinition --output=custom-columns=:.metadata.name | grep certmanager.k8s.io); do
  kubectl delete --wait=true customresourcedefinition ${item}
done

###################################################################################################

exit 0

###################################################################################################
